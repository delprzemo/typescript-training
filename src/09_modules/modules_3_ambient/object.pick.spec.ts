import { expect } from 'chai';

// https://github.com/jonschlinkert/object.pick
var pick = require('object.pick');

describe('Object pick ambient module', () => {
    
    // Advanced
    it('Run with one key', () => {
        let result = pick({a: 'a', b: 'b'}, 'a');

        expect(result).to.deep.equal({a: 'a'});
    });

    it('Run with multiple key', () => {
        let result = pick({a: 'a', b: 'b', c: 2}, ['a', 'c']);

        expect(result).to.deep.equal({a: 'a', c: 2});
    });

});